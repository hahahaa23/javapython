package ex0622;

public class EE {

	//ctrl + F11
	//ctrl + z 뒤로 가기
	//ctrl + y 앞으로 가기
	//ctrl + d 삭제
	
	public static void main(String[] args) {
		
		double pi = 3.14;
		int pia = (int) pi;
		
		System.out.println("pia = "+pia);
		
		double a = 3.123123d; //d는 대문자, 소문자 상관없음
		System.out.println("(int)a = "+(int)a);
		
		
	}
}
