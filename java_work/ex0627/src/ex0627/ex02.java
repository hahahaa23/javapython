package ex0627;

public class ex02 {

	public static void main(String[] args) {

		// ctrl + shift + f 자동정렬... 근데 괄호의 갯수가 맞지 않으면 정상적으로 작동안됨..
		int num = 5;

		System.out.print(num++);
		System.out.print(num);
		System.out.println();
		System.out.println(num--);
		System.out.println(num);
		
		System.out.println(++num);

	}
}
