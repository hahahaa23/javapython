package ex0630;

import java.util.Iterator;

public class ex01 {

	public static void main(String[] args) {
		
	int num = 0;
	while (num<5) {
		System.out.println("num ="+num);
		num++; //num++ --> 하나씩 증가
		}
		
	for (int num1 = 0; num1<5; num1++) {
		System.out.println("num1 = "+num1);
		//(선언; 조건; 변화)
	}
	}
	
}
